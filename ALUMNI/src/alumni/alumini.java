    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String id = moodle.getText();
        String name2 = name.getText();
        String dob2 = dob.getText();
        String age2 = age.getText();
        String gender2 = gender.getText();
        String email2 = email.getText();
        String mobile2 = mobile.getText();
        String year2 = year.getText();
        String degree2 = degree.getText();
        String skills2 = skills.getText();
        String linkdin2 = linkdin.getText();
        String master2 = master.getText();
        String mdegree2 = mdegree.getText();
        
        
        try {
            
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db", "root", "Mane2004@");

            PreparedStatement ps = con.prepareStatement("INSERT INTO alumniinfo (id,name,dob,age,gender,email,mobile,pass,degree,skills,linkdin,master,mdegree) VALUES (?, ?,?,?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1, id);
            ps.setString(2, name2);
            ps.setString(3, dob2);
            ps.setString(4, age2);
            ps.setString(5, gender2);
            ps.setString(6, email2);
            ps.setString(7, mobile2);
            ps.setString(8, year2);
            ps.setString(9, degree2);
            ps.setString(10, skills2);
            ps.setString(11, linkdin2);
            ps.setString(12, master2);
            ps.setString(13, mdegree2);

            
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                 JOptionPane.showMessageDialog(this, "Data Saved Succesfully");
            } else {
               JOptionPane.showMessageDialog(this, "Failed to save data");
            }
            
            
            con.close();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

