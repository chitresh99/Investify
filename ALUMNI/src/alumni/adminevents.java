    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
       String name2 = name.getText();
       String details2 = details.getText();
       String conduct2 = conduct.getText();
       String timedate2 = timedate.getText();
       String link2 = link.getText();
        Connection connection = null;
        try {

            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/db", "root", "Mane2004@");

                       String query = "INSERT INTO events (name,details,conducted,timedate,link ,entrytime) VALUES (?,?,?,?,?, NOW())";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, name2);
             preparedStatement.setString(2, details2);
              preparedStatement.setString(3, conduct2);
               preparedStatement.setString(4, timedate2);
                preparedStatement.setString(5, link2);

            preparedStatement.executeUpdate();

            preparedStatement.close();

            JOptionPane.showMessageDialog(this, "Announced successfully!");

        } catch (SQLException e) {
            // Handle any errors here
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while announcing.");
        } finally {

            try {
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException e) {

                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

