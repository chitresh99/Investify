    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.setRowCount(0);

        try {

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db", "root", "Mane2004@");

            Statement stmt = con.createStatement();

            ResultSet rs = stmt.executeQuery("SELECT moodleid, name, year ,branch,age, dob, mobileno, email,domain  FROM studentinfo");

            while (rs.next()) {
                String id = rs.getString("moodleid");
                String name2 = rs.getString("name");
                String year2 = rs.getString("year");
                String branch2 = rs.getString("branch");
                String age2 = rs.getString("age");
                String dob2 = rs.getString("dob");
                String mob2 = rs.getString("mobileno");
                String domain2 = rs.getString("domain");
                
                model.addRow(new Object[]{id, name2,year2,branch2,age2,dob2,mob2,domain2});
            }

            con.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

