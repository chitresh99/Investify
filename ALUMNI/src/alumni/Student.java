    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
         String id = moodle.getText();
        String name2 = name.getText();
        String year2 = year.getText();
        String branch2 = branch.getText();
        String age2 = age.getText();
        String dob2 = dob.getText();
        String mob2 = mob.getText();
        String email2 = email.getText();
        String domain2 = doamin.getText();

        
        try {
            
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db", "root", "Mane2004@");

            PreparedStatement ps = con.prepareStatement("INSERT INTO studentinfo (moodleid ,name ,year ,branch ,age ,dob ,mobileno ,email ,domain) VALUES (?, ?, ?, ?, ?, ?, ? , ?, ?)");
            //This line creates a prepared statement for executing an INSERT query. The SQL statement is parameterized with placeholders ("?") for values that will be supplied later.
            ps.setString(1, id);
             //ps.setString(1, id): Sets the first parameter (the moodle id column) to the value stored in the id variable.
            ps.setString(2, name2);
            ps.setString(3, year2);
            ps.setString(4, branch2);
            ps.setString(5, age2);
            ps.setString(6, dob2);
            ps.setString(7, mob2);
            ps.setString(8, email2);
            ps.setString(9, domain2);
           
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                 JOptionPane.showMessageDialog(this, "Data Saved Succesfully");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to save data");
            }

            
            con.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

