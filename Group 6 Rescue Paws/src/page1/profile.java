    public profile() {
        initComponents();
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

       
        jTable1.getColumnModel().getColumn(0).setCellRenderer(new ImageRenderer());

        try {
            Connection con = db.db_config();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from admimage");
            while (rs.next()) {
               
                model.addRow(new Object[] {rs.getString("dogid"), rs.getString("dogname"), rs.getBytes(1) });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

