     class ImageRenderer extends DefaultTableCellRenderer {
        @Override
        protected void setValue(Object value) {
            if (value instanceof byte[]) {
                byte[] imageData = (byte[]) value;
                try {
                    ByteArrayInputStream bis = new ByteArrayInputStream(imageData); // Converting the byte array to an Image
                    BufferedImage bImage = ImageIO.read(bis);
                    Image image = bImage.getScaledInstance(208, 212, Image.SCALE_SMOOTH);
                    setIcon(new ImageIcon(image));
                }
                    catch (Exception e) {
                   
                    e.printStackTrace();
                }
            } else {
              super.setValue(value); // Handle non-image data
            }
        }
     }

