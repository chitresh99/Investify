    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String id = jTextField1.getText();
       String name  = jTextField2.getText();
       String age = jTextField3.getText();
       String gender = jTextField4.getText();
       String trained = jTextField5.getText();
       
       
       try {
            pst = con.prepareStatement("INSERT INTO detailsadm(dogid,dogname,age,gender,housetrained)VALUES(?,?,?,?,?)");
            pst.setString(1,id);
            pst.setString(2,name);
            pst.setString(3,age);
            pst.setString(4,gender);
            pst.setString(5,trained);
       
        int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"You have successfully submitted!");
                jTextField1.setText("");
                 jTextField2.setText("");
                  jTextField3.setText("");
                  jTextField4.setText("");
                  jTextField5.setText("");
            }
             else{
            JOptionPane.showMessageDialog(this,"Failed");
        }
       }
       catch (SQLException ex) {
            Logger.getLogger(Adopt.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

