    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String Parentname = txtparent.getText();
       String Petname = txtname.getText();
       String email = txtemail.getText();
       String number = txtnumber.getText();
       String address = txtadd.getText();
      
       if (path == null) {
        JOptionPane.showMessageDialog(this, "Please select an image file.");
    } else {
        
        System.out.print("Image Path - "+path);
        System.out.print("Image Name - "+f.getName());
        File f = new File(path);
       }
        try {
             InputStream is = new FileInputStream(f);
            pst = con.prepareStatement("INSERT INTO adopt_1(parent,pet,email,number,address,imageName,imagePath,imageFile)VALUES(?,?,?,?,?,?,?,?)");
            pst.setString(1,Parentname);
            pst.setString(2,Petname);
            pst.setString(3,email);
            pst.setString(4,number);
            pst.setString(5,address);
             pst.setString(6, f.getName());
            pst.setString(7, path);
            pst.setBlob(8,is);
            
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"You have successfully submitted!");
                txtparent.setText("");
                 txtname.setText("");
                  txtemail.setText("");
                   txtnumber.setText("");
                    txtadd.setText("");
                
            }
            else{
            JOptionPane.showMessageDialog(this,"Failed");
            }
            
         new Adopt().setVisible(true); 
        } catch (SQLException ex) {
            Logger.getLogger(Adopt.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
        Logger.getLogger(Adopt.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

