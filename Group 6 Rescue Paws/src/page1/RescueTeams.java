    private void mumbaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mumbaiActionPerformed
        // TODO add your handling code here:
        if (mumbai.isSelected()) {
            try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/miniproject", "root", "Ra@238gs");
            String sql = "SELECT * FROM teamthane";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet resultSet = statement.executeQuery();
            TextArea.setText(""); // Clear previous results
            while (resultSet.next()) {
                String data = resultSet.getString("Region");
                String head = resultSet.getString("Region Head");
                String num = resultSet.getString("Contact");
                
               TextArea.append(data + "\n");
                TextArea.append(head + "\n");
                TextArea.append(num + "\n");
             
            }
            resultSet.close();
            statement.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
            TextArea.setText("An error occurred: " + e.getMessage());
        }
        }
    }//GEN-LAST:event_mumbaiActionPerformed

