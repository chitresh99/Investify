    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String name = txtname.getText();
       String number = txtno.getText();
       String purpose = txtpurpose.getText();
       String amount = amt.getText();
       String upiid = txtupi.getText();
       String sponsor = namesponsor.getText();
       
       try {
            pst = con.prepareStatement("INSERT INTO sponsordetails(name,Purpose,upiid,amount,SponsorName,ContactNo)VALUES(?,?,?,?,?,?)");
            pst.setString(1,name);
            pst.setString(2,purpose);
            pst.setString(3,upiid);
            pst.setString(4,amount);
            pst.setString(5,sponsor);
            pst.setString(6,number);
            
       
        int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"You have successfully submitted!");
                amt.setText("");
                 txtname.setText("");
                  namesponsor.setText("");
                   txtpurpose.setText("");
                   txtupi.setText("");
                   txtno.setText("");
            }
             else{
            JOptionPane.showMessageDialog(this,"Failed");
        }
       }
       catch (SQLException ex) {
            Logger.getLogger(Adopt.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

