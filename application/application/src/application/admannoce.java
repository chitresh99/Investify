    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String date = jTextField1.getText();
        String announcement = jTextArea2.getText();

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/app", "root", "@group1816");
            String query = "INSERT INTO announce (date, announce) VALUES (?, ?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, date);
            pst.setString(2, announcement);
            pst.executeUpdate();
            con.close();
            JOptionPane.showMessageDialog(this, "announcement posted successfully");
            
            admcourses adm = new admcourses();
            adm.setVisible(true);
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(admannoce.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "An error occurred while saving the announcement", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

