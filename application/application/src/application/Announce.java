    private void showtabledata() {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/app", "root", "@group1816");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM announce");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                Object[] row = {rs.getString("date"), rs.getString("announce")};
                model.addRow(row);
            }
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "An error occurred while fetching data", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
