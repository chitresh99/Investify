    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        String firstName = txtname.getText();
        String middleName = txtmname.getText();
        String lastName = txtlname.getText();
        String email = txteid.getText();
        String mobilenumber = txtmno.getText();
        String moodleId = txtmid.getText();
        java.util.Date dob = txtdob.getDate();
        double sscPercentage = Double.parseDouble(txtssc.getText());
        double hscPercentage = Double.parseDouble(txthsc.getText());
        String instituteName = txtinstname.getText();
        String branch = txtbranch.getText();
        int semester = Integer.parseInt(txtsem.getText());
        int passingYear = Integer.parseInt(txtpass.getText());
        double cgpa = Double.parseDouble(txtcgpa.getText());

        try {
            // Establish a connection
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/app", "root", "@group1816");

            // Create a PreparedStatement
            PreparedStatement ps = con.prepareStatement("INSERT INTO profile (firstName, middleName, lastName, email, mobilenumber, moodleId, dob, sscPercentage, hscPercentage, instituteName, branch, semester, passingYear, cgpa) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
            ps.setString(1, firstName);
            ps.setString(2, middleName);
            ps.setString(3, lastName);
            ps.setString(4, email);
            ps.setString(5, mobilenumber);
            ps.setString(6, moodleId);
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate = formatter.format(dob);
            ps.setString(7,formattedDate);
            ps.setDouble(8, sscPercentage);
            ps.setDouble(9, hscPercentage);
            ps.setString(10, instituteName);
            ps.setString(11, branch);
            ps.setInt(12, semester);
            ps.setInt(13, passingYear);
            ps.setDouble(14, cgpa);

            // Execute the query
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                System.out.println("Data saved successfully!");
            } else {
                System.out.println("Failed to save data!");
            }

            // Close the connection
            con.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_savebtnActionPerformed

