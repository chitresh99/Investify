    private void displayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        try {
        
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/app", "root", "@group1816");

        
        Statement stmt = con.createStatement();
        
        ResultSet rs = stmt.executeQuery("SELECT topic , queries FROM queries");
         
        while (rs.next()) {
            String topic = rs.getString("topic");
            String queries = rs.getString("queries");
            
            model.addRow(new Object[]{topic , queries});
    }//GEN-LAST:event_displayActionPerformed
     con.close();

    } catch (Exception e) {
        e.printStackTrace();
    }
    }                                             

