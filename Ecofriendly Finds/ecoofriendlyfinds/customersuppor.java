    public void savedata() {
        try {
            String str = "INSERT INTO suggestion (suggestions) VALUES (?)";
            pst = conn.prepareStatement(str);

            // Assuming you have a JTextArea named txtsuggestion for the suggestion input
            pst.setString(1, Logger.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "Suggestion Saved Successfully!", "Done!", JOptionPane.INFORMATION_MESSAGE);
            clearfields();

        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

