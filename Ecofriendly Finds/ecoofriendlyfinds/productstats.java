    private void fetchDataFromDatabase() {
        tableModel.setRowCount(0);  // Clear existing data in the table

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/databasemain", "root", "ARYA#305#varun");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT productname, viewcount FROM display");

            while (rs.next()) {
                String productname = rs.getString("productname");
                String viewcount = rs.getString("viewcount");
                tableModel.addRow(new Object[]{productname, viewcount});
            }

            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

