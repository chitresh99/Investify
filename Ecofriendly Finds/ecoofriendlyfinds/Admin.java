      private void fetchDataFromDatabase() {
        tableModel.setRowCount(0);  // Clear existing data in the table

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/databasemain", "root", "ARYA#305#varun");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT username, product FROM searchedhistory");

            while (rs.next()) {
                String username = rs.getString("username");
                String product = rs.getString("product");
                tableModel.addRow(new Object[]{username, product});
            }

            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

