    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1) {
            // Get the model of the JTable
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            // Get the data from the selected row, assuming product ID is in the first column
            String productID = (String) model.getValueAt(selectedRow, 0);

            // Delete the corresponding row from the database and JTable
            if (deleteFromDatabase(productID)) {
                model.removeRow(selectedRow);
            }
        }
    }

    private boolean deleteFromDatabase(String productID) {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/databasemain", "root", "ARYA#305#varun");
            Statement stmt = con.createStatement();
            String deleteQuery = "DELETE FROM wishlist WHERE productid = '" + productID + "'";
            int rowsAffected = stmt.executeUpdate(deleteQuery);
            con.close();

            return rowsAffected > 0;
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

