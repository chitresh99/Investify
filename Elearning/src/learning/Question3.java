    private void btnCheckAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckAnswerActionPerformed

        // Check if the user has selected an option. Otherwise, show an error message.
        if ( !jRadioButton2_.isSelected() && !jRadioButton1_.isSelected() && !jRadioButton3_.isSelected() && !jRadioButton4_.isSelected()) {
            JOptionPane.showMessageDialog(null, "Please Select an answer");
        } else {
            // Code for what happens when the user selects the right or wrong answer.
            if (jRadioButton2_.isSelected()) {
                lbl.setText("Correct Answer");
                lbl.setForeground(Color.GREEN);
            } else {
                lbl.setText("Incorrect - Answer is C)[1, ∞]");
                lbl.setForeground(Color.RED);
            }

            ButtonGroup buttonGroup = new ButtonGroup();
            buttonGroup.add(jRadioButton2_);
            buttonGroup.add(jRadioButton1_);
            buttonGroup.add(jRadioButton3_);
            buttonGroup.add(jRadioButton4_);

            // To disable all radio buttons in the group:
            buttonGroup.getElements().nextElement().setEnabled(false);
        }
    }//GEN-LAST:event_btnCheckAnswerActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
   // TODO add your handling code here:
       //prevent goto next question without selecting and answer
       if(!jRadioButton1_.isSelected() && !jRadioButton2_.isSelected() && !jRadioButton3_.isSelected() && !jRadioButton4_.isSelected() ){
           JOptionPane.showMessageDialog(null, "Please Select and answer");
       }else{
           //check correct answer
           if (jRadioButton2_.isSelected()) {
               marks = marks + 1;
           }
           quizanswered = quizanswered + 1;
           // call for next question window with passing value
           new Question4(quizanswered, marks).setVisible(true);
           this.dispose();
       }
    }//GEN-LAST:event_btnSubmitActionPerformed

